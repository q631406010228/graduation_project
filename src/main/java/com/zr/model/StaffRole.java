package com.zr.model;
/**
 * 教师角色表
 * @author Administrator
 *
 */
public class StaffRole {
	private int trid; //教师角色ID
	private int stid; //教职工ID
	private int rid; //角色ID
	public int getTrid() {
		return trid;
	}
	public void setTrid(int trid) {
		this.trid = trid;
	}
	public int getStid() {
		return stid;
	}
	public void setStid(int stid) {
		this.stid = stid;
	}
	public int getRid() {
		return rid;
	}
	public void setRid(int rid) {
		this.rid = rid;
	}
	
}
